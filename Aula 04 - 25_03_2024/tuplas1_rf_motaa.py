#tudo que aprendemos em lista pode ser usado em tuplas
#EXCETO, o fato de que tuplas, quando criadas, não aceitam modificações
# TUPLAS SÃO IMUTÁVEIS!!!

#a sintaxe da tupla utiliza ( ,)
#maneira implícita
a = ("elemento1",)
b = ("elemento1", "elemento2")
print(a)
print(b)

#maneira explícita
c = tuple(["elemento1", "elemento2", "elemento3"])
print(c)

#tuplas são imutáveis, mas são organizáveis